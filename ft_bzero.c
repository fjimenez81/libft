#include <string.h>
#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	char	*c;
	size_t	i;

	c = (char *)str;
	i = 0;
	while (i < n)
	{
		*c = '\0';
		c++;
		i++;
	}
}
